// Enqueue necessary scripts and styles
function rcs_enqueue_scripts() {
    // Define the version number
    $css_version = '1.0.18';
    
    // Enqueue the CSS file with versioning
    wp_enqueue_style('rcs-style', plugins_url('assets/css/rcs-style.css', __FILE__), array(), $css_version);
    
    // Enqueue the JavaScript file
    wp_enqueue_script('rcs-script', plugins_url('assets/js/rcs-script.js', __FILE__), array('jquery'), false, true);
}
add_action('wp_enqueue_scripts', 'rcs_enqueue_scripts', 20);

